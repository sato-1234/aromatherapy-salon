	<h3 class="mintyo">アロマテラピーとは</h3>
	<p class="hiragino">アロマテラピーは、植物から取り出した精油（芳香物質）を使って、心身の健康を維持し、病気にならないようにするために、昔から使われてきたものです。におい物質（精油）の中には免疫力を高める作用があることも判ってきています。リラックスを求め好きな香りを嗅ぐことは、心理的効果ばかりでなく、免疫力を高め、ストレスに耐えうる身体を作るのによいと言われています。精油はいろいろな組織に影響を与え、最終的には肝臓で分解され、尿や汗の中に排出されますが、消化器系を介さないため、胃などに障害を起こす恐れはありません。アロマトリートメントは、鼻から心に、皮膚から身体に働きかけ、自然治癒力を高める作用があります。</p>
